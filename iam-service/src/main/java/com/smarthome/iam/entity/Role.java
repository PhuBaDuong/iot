package com.smarthome.iam.entity;

import jakarta.persistence.Column;
import jakarta.persistence.Entity;
import jakarta.persistence.GeneratedValue;
import jakarta.persistence.GenerationType;
import jakarta.persistence.Id;
import jakarta.persistence.Table;

/**
 * =============================================================================
 * Role - an RBAC role (Phase 3A)
 * =============================================================================
 * The platform defines three roles: {@code ADMIN}, {@code OPERATOR}, and
 * {@code VIEWER}. Role names are stored WITHOUT the {@code ROLE_} prefix; that
 * prefix is added when mapping to Spring Security authorities.
 * =============================================================================
 */
@Entity
@Table(name = "roles")
public class Role {

    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Long id;

    @Column(nullable = false, unique = true, length = 50)
    private String name;

    @Column(length = 255)
    private String description;

    protected Role() {
        // for JPA
    }

    public Role(String name, String description) {
        this.name = name;
        this.description = description;
    }

    public Long getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }
}
